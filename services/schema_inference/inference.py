"""Schema inference engine.

Three execution modes, all returning the same `SchemaInferenceResult` contract:

1. **Bedrock mode**: Claude via Amazon Bedrock. Needs `bedrock:InvokeModel` on the model id.
2. **Anthropic mode**: Claude via api.anthropic.com directly. Needs `ANTHROPIC_API_KEY`.
3. **Local mock mode**: deterministic heuristic classifier — same output shape, no network.

`auto` (default) tries bedrock → anthropic → local in order, falling back on any failure.
"""

from __future__ import annotations

import json
import logging
import os
import re
from dataclasses import asdict, dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from .prompts import (
    CANONICAL_FIELDS,
    PII_TIERS,
    PROMPT_VERSION,
    SYSTEM_PROMPT,
    build_user_prompt,
)

log = logging.getLogger(__name__)

# Cross-region inference profile (us.* prefix is AWS-managed and required for
# Claude 4.x family on Bedrock — direct on-demand model IDs are no longer accepted
# for these models). The profile auto-routes to the nearest region with capacity.
DEFAULT_BEDROCK_MODEL = "us.anthropic.claude-sonnet-4-6"
DEFAULT_ANTHROPIC_MODEL = "claude-sonnet-4-6"
DEFAULT_REGION = "us-east-1"


@dataclass
class ColumnInference:
    source_column: str
    canonical_field: str
    confidence: float
    pii_tier: str
    cleansing_rules: list[str] = field(default_factory=list)
    reasoning: str = ""


@dataclass
class SchemaInferenceResult:
    filename: str
    detected_format: str
    columns: list[ColumnInference]
    overall_quality_risk: str
    overall_quality_notes: str
    suggested_partition_column: str | None
    prompt_version: str
    model_id: str
    inferred_at: str
    mode: str  # "bedrock" or "local_mock"

    def to_dict(self) -> dict[str, Any]:
        d = asdict(self)
        return d

    def to_data_contract_yaml(self) -> str:
        """Render the inference as a draft data contract YAML for human review."""
        lines = [
            f"# DRAFT data contract — review before promoting to production",
            f"# Generated by schema-inference {self.prompt_version} ({self.mode})",
            f"# Inferred at: {self.inferred_at}",
            f"",
            f"partner_id: REPLACE_ME",
            f"contract_version: 1",
            f"source_format: {self.detected_format}",
            f"partition_column: {self.suggested_partition_column or 'effective_start_date'}",
            f"overall_quality_risk: {self.overall_quality_risk}",
            f"reviewed_by: REPLACE_ME",
            f"reviewed_at: REPLACE_ME",
            f"",
            f"columns:",
        ]
        for c in self.columns:
            lines.append(f"  - source_column: \"{c.source_column}\"")
            lines.append(f"    canonical_field: {c.canonical_field}")
            lines.append(f"    confidence: {c.confidence:.2f}")
            lines.append(f"    pii_tier: {c.pii_tier}")
            if c.cleansing_rules:
                lines.append(f"    cleansing_rules:")
                for rule in c.cleansing_rules:
                    lines.append(f"      - {rule}")
            lines.append(f"    reasoning: \"{c.reasoning}\"")
        return "\n".join(lines)


def infer_schema(
    filename: str,
    sample_rows: list[dict[str, Any]],
    *,
    mode: str | None = None,
    model_id: str | None = None,
    region: str | None = None,
) -> SchemaInferenceResult:
    """Infer a draft schema for an unknown partner file.

    Args:
        filename: name of the file (used for context in the prompt)
        sample_rows: a list of dicts representing the first ~50 rows of the file
        mode: "bedrock" or "local" — overrides env-driven default
        model_id: Bedrock model identifier; defaults to Claude Sonnet 4.6
        region: AWS region for Bedrock; defaults to us-east-1

    Returns:
        SchemaInferenceResult with column mappings and quality assessment.
    """
    if not sample_rows:
        raise ValueError("sample_rows cannot be empty")

    chosen_mode = (mode or os.environ.get("LORE_SCHEMA_INFERENCE_MODE", "auto")).lower()
    chosen_model = model_id or os.environ.get("LORE_BEDROCK_MODEL", DEFAULT_BEDROCK_MODEL)
    chosen_region = region or os.environ.get("AWS_REGION", DEFAULT_REGION)

    if chosen_mode == "local":
        return _infer_local(filename, sample_rows, chosen_model)

    if chosen_mode == "bedrock":
        return _infer_bedrock(filename, sample_rows, chosen_model, chosen_region)

    if chosen_mode == "anthropic":
        anthro_model = os.environ.get("LORE_ANTHROPIC_MODEL", DEFAULT_ANTHROPIC_MODEL)
        return _infer_anthropic(filename, sample_rows, anthro_model)

    # auto mode: try bedrock → anthropic → local
    try:
        return _infer_bedrock(filename, sample_rows, chosen_model, chosen_region)
    except Exception as exc:
        log.warning("Bedrock inference failed (%s), trying Anthropic API", exc)
    if os.environ.get("ANTHROPIC_API_KEY"):
        try:
            anthro_model = os.environ.get("LORE_ANTHROPIC_MODEL", DEFAULT_ANTHROPIC_MODEL)
            return _infer_anthropic(filename, sample_rows, anthro_model)
        except Exception as exc:
            log.warning("Anthropic API inference failed (%s), falling back to local mock", exc)
    return _infer_local(filename, sample_rows, chosen_model)


def _infer_bedrock(
    filename: str,
    sample_rows: list[dict[str, Any]],
    model_id: str,
    region: str,
) -> SchemaInferenceResult:
    """Call Bedrock Claude with the structured prompt. Returns the parsed result."""
    try:
        import boto3  # imported lazily so local mode works without AWS deps
    except ImportError as e:
        raise RuntimeError("boto3 is required for Bedrock mode") from e

    client = boto3.client("bedrock-runtime", region_name=region)
    user_prompt = build_user_prompt(filename, sample_rows[:50], CANONICAL_FIELDS, PII_TIERS)

    body = {
        "anthropic_version": "bedrock-2023-05-31",
        "max_tokens": 4000,
        "system": SYSTEM_PROMPT,
        "messages": [{"role": "user", "content": user_prompt}],
        "temperature": 0.0,  # deterministic for the structured-output use case
    }

    resp = client.invoke_model(
        modelId=model_id,
        body=json.dumps(body),
        contentType="application/json",
        accept="application/json",
    )
    payload = json.loads(resp["body"].read())
    text = payload["content"][0]["text"]
    parsed = _extract_json(text)
    return _result_from_dict(filename, parsed, model_id, mode="bedrock")


def _infer_anthropic(
    filename: str,
    sample_rows: list[dict[str, Any]],
    model_id: str,
) -> SchemaInferenceResult:
    """Call Claude via the Anthropic API directly. Same prompt + parsing as Bedrock path."""
    import urllib.request
    import urllib.error

    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        raise RuntimeError("ANTHROPIC_API_KEY is not set")

    user_prompt = build_user_prompt(filename, sample_rows[:50], CANONICAL_FIELDS, PII_TIERS)
    body = {
        "model": model_id,
        "max_tokens": 4000,
        "system": SYSTEM_PROMPT,
        "messages": [{"role": "user", "content": user_prompt}],
        "temperature": 0.0,
    }
    req = urllib.request.Request(
        "https://api.anthropic.com/v1/messages",
        data=json.dumps(body).encode("utf-8"),
        headers={
            "x-api-key": api_key,
            "anthropic-version": "2023-06-01",
            "content-type": "application/json",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=60) as resp:
            payload = json.loads(resp.read())
    except urllib.error.HTTPError as e:
        detail = e.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"Anthropic API HTTP {e.code}: {detail[:300]}") from e

    text = payload["content"][0]["text"]
    parsed = _extract_json(text)
    return _result_from_dict(filename, parsed, model_id, mode="anthropic")


def _result_from_dict(
    filename: str, parsed: dict, model_id: str, *, mode: str
) -> SchemaInferenceResult:
    cols = [
        ColumnInference(
            source_column=c["source_column"],
            canonical_field=c["canonical_field"],
            confidence=float(c["confidence"]),
            pii_tier=c["pii_tier"],
            cleansing_rules=list(c.get("cleansing_rules", [])),
            reasoning=c.get("reasoning", ""),
        )
        for c in parsed["columns"]
    ]
    return SchemaInferenceResult(
        filename=filename,
        detected_format=parsed.get("detected_format", "unknown"),
        columns=cols,
        overall_quality_risk=parsed.get("overall_quality_risk", "MEDIUM"),
        overall_quality_notes=parsed.get("overall_quality_notes", ""),
        suggested_partition_column=parsed.get("suggested_partition_column"),
        prompt_version=PROMPT_VERSION,
        model_id=model_id,
        inferred_at=datetime.now(timezone.utc).isoformat(),
        mode=mode,
    )


def _extract_json(text: str) -> dict:
    """Robustly extract a JSON object from an LLM response.

    Claude is prompted for JSON-only output, but in practice we still see occasional
    leading/trailing whitespace or markdown fences. We strip those and try to parse.
    """
    text = text.strip()
    # Strip ```json ... ``` if present
    fenced = re.match(r"```(?:json)?\s*(.*?)\s*```", text, re.DOTALL)
    if fenced:
        text = fenced.group(1)
    # Find first { and last } as a last resort
    start = text.find("{")
    end = text.rfind("}")
    if start >= 0 and end > start:
        text = text[start : end + 1]
    return json.loads(text)


