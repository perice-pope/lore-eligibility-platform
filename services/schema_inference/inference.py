# Pattern → (canonical_field, pii_tier, cleansing_rules, reasoning)
_HEURISTICS: list[tuple[re.Pattern, str, str, list[str], str]] = [
    (re.compile(r"^(ssn|social|tax_id|tin)$", re.I), "ssn", "TIER_1_DIRECT",
     ["validate_ssn_format", "tokenize_via_skyflow", "store_last_4_only"],
     "Column header indicates SSN."),
    (re.compile(r"^(dob|date_of_birth|birth_date|birthday)$", re.I), "dob", "TIER_1_DIRECT",
     ["coerce_iso8601_date"], "Column header indicates date of birth."),
    (re.compile(r"^(first_?name|fname|given_?name)$", re.I), "first_name", "TIER_1_DIRECT",
     ["strip_whitespace", "title_case_with_diacritics"], "Header indicates first name."),
    (re.compile(r"^(last_?name|lname|surname|family_?name)$", re.I), "last_name", "TIER_1_DIRECT",
     ["strip_whitespace", "title_case_with_diacritics"], "Header indicates last name."),
    (re.compile(r"^(middle_?name|mname|middle_initial|mi)$", re.I), "middle_name", "TIER_1_DIRECT",
     ["strip_whitespace"], "Header indicates middle name."),
    (re.compile(r"^(email|email_address|e_mail)$", re.I), "email", "TIER_1_DIRECT",
     ["lowercase", "rfc5322_validate", "tokenize_via_skyflow"], "Header indicates email."),
    (re.compile(r"^(phone|phone_number|tel|mobile)$", re.I), "phone", "TIER_1_DIRECT",
     ["e164_normalize", "tokenize_via_skyflow"], "Header indicates phone number."),
    (re.compile(r"^(addr|address|street|address_?line_?1|address1)$", re.I), "address_line_1",
     "TIER_1_DIRECT", ["usps_validate", "tokenize_via_skyflow"], "Header indicates street address."),
    (re.compile(r"^(addr2|address_?line_?2|address2|apt|unit|suite)$", re.I), "address_line_2",
     "TIER_1_DIRECT", ["usps_validate"], "Header indicates address line 2."),
    (re.compile(r"^city$", re.I), "city", "TIER_2_QUASI",
     ["title_case", "usps_validate"], "Header indicates city."),
    (re.compile(r"^(state|st|province)$", re.I), "state", "TIER_2_QUASI",
     ["uppercase", "validate_us_state_abbrev"], "Header indicates state."),
    (re.compile(r"^(zip|zipcode|postal_?code)$", re.I), "zip", "TIER_2_QUASI",
     ["pad_to_5_digits"], "Header indicates ZIP."),
    (re.compile(r"^(zip4|plus4|zip_plus4)$", re.I), "zip4", "TIER_2_QUASI",
     [], "Header indicates ZIP+4."),
    (re.compile(r"^(member_?id|partner_?member_?id|memberid|external_?id|emp(loyee)?_?id|subscriber_?id)$", re.I),
     "partner_member_id", "TIER_4_NONE",
     ["strip_whitespace"], "Header indicates partner-side member identifier."),
    (re.compile(r"^(gender|sex)$", re.I), "gender", "TIER_2_QUASI",
     ["normalize_to_M_F_X_U"], "Header indicates gender."),
    # Match anywhere in the header (substring) — handles "EligStartDate", "CoverageStartDate", "Effective_Start", etc.
    (re.compile(r"(elig|effective|coverage)[_ ]?start|^start[_ ]?date$", re.I),
     "effective_start_date", "TIER_3_SENSITIVE",
     ["coerce_iso8601_date"], "Header indicates eligibility/coverage start."),
    (re.compile(r"(elig|effective|coverage|term)[_ ]?(end|term)|^end[_ ]?date$|^termination$", re.I),
     "effective_end_date", "TIER_3_SENSITIVE",
     ["coerce_iso8601_date"], "Header indicates eligibility/coverage end."),
    (re.compile(r"^(plan|plan_?code|product|product_?code)$", re.I), "plan_code", "TIER_3_SENSITIVE",
     ["uppercase"], "Header indicates plan/product code."),
    (re.compile(r"^(employer|employer_?name|company|group)$", re.I), "employer_name", "TIER_2_QUASI",
     ["strip_whitespace"], "Header indicates employer."),
    (re.compile(r"^(suffix|name_suffix)$", re.I), "suffix", "TIER_1_DIRECT",
     [], "Header indicates name suffix."),
]

# Value-shape patterns (used when header is ambiguous)
_SSN_RE = re.compile(r"^\d{3}-?\d{2}-?\d{4}$")
_DOB_RE = re.compile(r"^\d{4}[-/]\d{1,2}[-/]\d{1,2}$|^\d{1,2}[-/]\d{1,2}[-/]\d{2,4}$")
_EMAIL_RE = re.compile(r"^[^@\s]+@[^@\s]+\.[^@\s]+$")
_ZIP_RE = re.compile(r"^\d{5}(-\d{4})?$")
_PHONE_RE = re.compile(r"^[\+\-\.\(\) \d]{7,20}$")


def _infer_local(
    filename: str, sample_rows: list[dict[str, Any]], model_id: str
) -> SchemaInferenceResult:
    """Heuristic classifier. Mimics Bedrock output schema."""
    headers = list(sample_rows[0].keys())
    columns: list[ColumnInference] = []

    for header in headers:
        match = next((h for h in _HEURISTICS if h[0].search(header)), None)
        if match:
            _, canon, tier, rules, reason = match
            columns.append(
                ColumnInference(
                    source_column=header,
                    canonical_field=canon,
                    confidence=0.92,
                    pii_tier=tier,
                    cleansing_rules=list(rules),
                    reasoning=reason,
                )
            )
            continue

        # Header didn't match. Sniff values.
        values = [str(r.get(header, "")) for r in sample_rows[:20] if r.get(header)]
        canon, tier, rules, reason, conf = _sniff_values(values)
        columns.append(
            ColumnInference(
                source_column=header,
                canonical_field=canon,
                confidence=conf,
                pii_tier=tier,
                cleansing_rules=rules,
                reasoning=reason,
            )
        )

    overall_risk = _overall_risk(columns)
    detected_format = _detect_format(filename)
    suggested_partition = _suggest_partition(columns)

    return SchemaInferenceResult(
        filename=filename,
        detected_format=detected_format,
        columns=columns,
        overall_quality_risk=overall_risk,
        overall_quality_notes=(
            "Local heuristic inference. Review every column with confidence < 0.85 "
            "and re-run with Bedrock when AWS access is available for production promotion."
        ),
        suggested_partition_column=suggested_partition,
        prompt_version=PROMPT_VERSION,
        model_id=model_id,
        inferred_at=datetime.now(timezone.utc).isoformat(),
        mode="local_mock",
    )


def _sniff_values(values: list[str]) -> tuple[str, str, list[str], str, float]:
    if not values:
        return ("ignore", "TIER_4_NONE", [], "Empty column.", 0.5)
    sample = values[: min(len(values), 20)]
    if all(_SSN_RE.match(v) for v in sample):
        return ("ssn", "TIER_1_DIRECT",
                ["validate_ssn_format", "tokenize_via_skyflow"],
                "Values match SSN pattern.", 0.85)
    if all(_EMAIL_RE.match(v) for v in sample):
        return ("email", "TIER_1_DIRECT",
                ["lowercase", "tokenize_via_skyflow"], "Values look like emails.", 0.85)
    if all(_DOB_RE.match(v) for v in sample):
        return ("dob", "TIER_1_DIRECT", ["coerce_iso8601_date"],
                "Values look like dates.", 0.7)
    if all(_ZIP_RE.match(v) for v in sample):
        return ("zip", "TIER_2_QUASI", ["pad_to_5_digits"],
                "Values look like US ZIPs.", 0.8)
    if all(_PHONE_RE.match(v) for v in sample):
        return ("phone", "TIER_1_DIRECT", ["e164_normalize", "tokenize_via_skyflow"],
                "Values look like phone numbers.", 0.65)
    return ("ignore", "TIER_4_NONE", [],
            "No strong signal from header or values; flagging for human review.", 0.4)


def _overall_risk(columns: list[ColumnInference]) -> str:
    pii_count = sum(1 for c in columns if c.pii_tier == "TIER_1_DIRECT")
    low_conf = sum(1 for c in columns if c.confidence < 0.7)
    if low_conf > len(columns) / 3:
        return "HIGH"
    if pii_count >= 4 or low_conf > 2:
        return "MEDIUM"
    return "LOW"


def _detect_format(filename: str) -> str:
    suffix = Path(filename).suffix.lower()
    return {
        ".csv": "csv",
        ".tsv": "tsv",
        ".json": "json",
        ".jsonl": "jsonl",
        ".ndjson": "jsonl",
        ".edi": "x12_834",
        ".834": "x12_834",
        ".txt": "fixed_width",
    }.get(suffix, "other")


def _suggest_partition(columns: list[ColumnInference]) -> str | None:
    for c in columns:
        if c.canonical_field == "effective_start_date":
            return c.source_column
    for c in columns:
        if c.canonical_field == "dob":
            return c.source_column
    return None
